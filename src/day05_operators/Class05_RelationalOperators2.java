package day05_operators;

public class Class05_RelationalOperators2 {
    public static void main(String[] args){

        // Equal operators

        System.out.println(1000==2000); // false
        System.out.println("Java" == "Java"); // true
        System.out.println("java" == "Java"); // false, case sensitive
        System.out.println("Muhtar" == "Good Guy"); // false
        System.out.println(true == false); // false

        System.out.println();
        System.out.println("-----------------------");

        // Not equal operators

        System.out.println(1000!=2000); // true
        System.out.println("Java" != "Java"); // false
        System.out.println("java" != "Java"); // true, case sensitive
        System.out.println("Muhtar" != "Good Guy"); // true
        System.out.println(true != false); // true



    }//main close
}//class close
