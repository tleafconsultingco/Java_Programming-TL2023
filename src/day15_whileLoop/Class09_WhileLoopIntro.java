package day15_whileLoop;

public class Class09_WhileLoopIntro {
    public static void main(String[] args) {

        if(true){
            System.out.println("Hello World"); // executed 1 time
        } // (if statement close)

        System.out.println("------");

        while(true){
            System.out.println("Hello Cydeo"); // executed infinite amount of times
        } // (while loop close)
    } // (main close)
} // (class close)
