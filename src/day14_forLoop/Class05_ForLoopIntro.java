package day14_forLoop;

public class Class05_ForLoopIntro {

    public static void main(String[] args) {

        System.out.println("Hello World"); // will be executed one time

        // to repeat 10x manually
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");

        System.out.println();
        System.out.println("--------");
        // what if I need to repeat MANY TIMES? Create a loop

        for(int i = 1; i<=10;i++){
            // start with 1, stop at 10
            // use increment to increase i by 1
            System.out.println("Hello Word"); // will continually print
        }




    }


}
