package day02_escape_sequences;

public class Class04_EscapeSequences {
    public static void main(String[] args) {
        // This creates new line without a new string
        System.out.println("Hello students\nToday we have Java class");

        System.out.println("-----------------------------------------");

        // This creates a paragraph indent
        System.out.println("\tJava Programming Language");
        // Place the \t escape sequence for more indentations
        System.out.println("\t\tJava Developer Course Batch 1");

        System.out.println("----------------------------------------");

        // This creates a single backslash to be printed
        System.out.println("\\");

        System.out.println("----------------------------------------");

        // This creates a double qoute
        System.out.println("My favorite TV show is \"The Walking Dead\"");

    }

}

    /*
    Escape Sequences
        1. \n is a New Line
        2. \t is a paragraph indent/space
        3. \\ is for a Back slash
        4. \" si for a double quote
     */






